    private void inserirNumero(String numero) {
        if(!"".equals(operacao) && !isInserindoNumero){
            temp="";
            isInserindoNumero = true;
        }
        
        if(igualPressionado){
            temp = "";
            igualPressionado = false;
        }
        if("".equals(temp) && ".".equals(numero)){
            temp = "0";
        }
        if(temp.contains(".") && ".".equals(numero)){
            return;
        }
        
        temp += numero;
        txtvisor.setText(temp);
    }

