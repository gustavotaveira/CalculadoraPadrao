    private void ativarOperacao(String operacao) {
        igualPressionado = false;
        isInserindoNumero = false;
        if("".equals(this.operacao)){
            if("".equals(temp)){
                return;
            }
            this.operacao = operacao;
            num1 = temp;
        }else{
            double[] d = converte(num1, temp);
            executaCalculo(d, this.operacao);
            this.operacao = operacao;
            txtvisor.setText(temp);
        }
    }

    private void btigualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btigualActionPerformed
        num2 = temp;
        double[] d = converte(num1, num2);
        executaCalculo(d, operacao);
        txtvisor.setText(temp);
        operacao = "";
        igualPressionado = true;
    }//GEN-LAST:event_btigualActionPerformed

    private void executaCalculo(double[] d, String operacao) {
        switch(operacao){ 
            case "+":
                num1 = temp = String.valueOf(d[0] + d[1]);
                break;
            case "-":
                num1 = temp = String.valueOf(d[0] - d[1]);
                break;
            case "x":
                num1 = temp = String.valueOf(d[0] * d[1]);
                break;
            case "/":
                num1 = temp = String.valueOf(d[0] / d[1]);
                break;
                
        }
    }

